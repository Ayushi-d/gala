public class CateringList {


    private String Description;

    private String CateringProductID;

    private String StartingPrice;

    private String StartingPriceAr;

    private String ThumbImage;

    private String CateringProductName;

    private String CateringProductNameAr;

    private String DescriptionAr;

    public String getDescription ()
    {
        return Description;
    }

    public void setDescription (String Description)
    {
        this.Description = Description;
    }

    public String getCateringProductID ()
    {
        return CateringProductID;
    }

    public void setCateringProductID (String CateringProductID)
    {
        this.CateringProductID = CateringProductID;
    }

    public String getStartingPrice ()
    {
        return StartingPrice;
    }

    public void setStartingPrice (String StartingPrice)
    {
        this.StartingPrice = StartingPrice;
    }

    public String getStartingPriceAr ()
    {
        return StartingPriceAr;
    }

    public void setStartingPriceAr (String StartingPriceAr)
    {
        this.StartingPriceAr = StartingPriceAr;
    }

    public String getThumbImage ()
    {
        return ThumbImage;
    }

    public void setThumbImage (String ThumbImage)
    {
        this.ThumbImage = ThumbImage;
    }

    public String getCateringProductName ()
    {
        return CateringProductName;
    }

    public void setCateringProductName (String CateringProductName)
    {
        this.CateringProductName = CateringProductName;
    }

    public String getCateringProductNameAr ()
    {
        return CateringProductNameAr;
    }

    public void setCateringProductNameAr (String CateringProductNameAr)
    {
        this.CateringProductNameAr = CateringProductNameAr;
    }

    public String getDescriptionAr ()
    {
        return DescriptionAr;
    }

    public void setDescriptionAr (String DescriptionAr)
    {
        this.DescriptionAr = DescriptionAr;
    }

}

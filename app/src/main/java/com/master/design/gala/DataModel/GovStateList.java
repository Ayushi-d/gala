package com.master.design.gala.DataModel;

public class GovStateList
{
    private String GovStateID;

    private String Show;

    private String GovStateName;

    private String GovStateNameAr;

    public String getGovStateID ()
    {
        return GovStateID;
    }

    public void setGovStateID (String GovStateID)
    {
        this.GovStateID = GovStateID;
    }

    public String getShow ()
    {
        return Show;
    }

    public void setShow (String Show)
    {
        this.Show = Show;
    }

    public String getGovStateName ()
    {
        return GovStateName;
    }

    public void setGovStateName (String GovStateName)
    {
        this.GovStateName = GovStateName;
    }

    public String getGovStateNameAr ()
    {
        return GovStateNameAr;
    }

    public void setGovStateNameAr (String GovStateNameAr)
    {
        this.GovStateNameAr = GovStateNameAr;
    }
}

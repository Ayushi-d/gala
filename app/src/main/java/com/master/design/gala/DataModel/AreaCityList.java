    {
        return AreaCityName;
    }

    public void setAreaCityName (String AreaCityName)
    {
        this.AreaCityName = AreaCityName;
    }

    public String getAreaCityID ()
    {
        return AreaCityID;
    }

    public void setAreaCityID (String AreaCityID)
    {
        this.AreaCityID = AreaCityID;
    }

    public String getShow ()
    {
        return Show;
    }

    public void setShow (String Show)
    {
        this.Show = Show;
    }

    public String getAreaCityNameAr ()
    {
        return AreaCityNameAr;
    }

    public void setAreaCityNameAr (String AreaCityNameAr)
    {
        this.AreaCityNameAr = AreaCityNameAr;
    }
